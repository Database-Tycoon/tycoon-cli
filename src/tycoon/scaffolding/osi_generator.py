def osi_schema_path() -> Path:
    """Path to the vendored OSI v0.1.1 JSON Schema file."""
    return Path(__file__).parent / "schemas" / "osi-v0.1.1.schema.json"


def validate_osi_yaml(yaml_path: Path) -> list[str]:
    """Validate a YAML file against the vendored OSI v0.1.1 schema.

    Returns a list of human-readable error messages. Empty list = valid.
    """
    import json

    from jsonschema import Draft202012Validator

    if not yaml_path.exists():
        return [f"OSI file not found: {yaml_path}"]

    try:
        body = yaml.safe_load(yaml_path.read_text())
    except yaml.YAMLError as exc:
        return [f"YAML parse error: {exc}"]

    schema = json.loads(osi_schema_path().read_text())
    validator = Draft202012Validator(schema)
    errors = sorted(validator.iter_errors(body), key=lambda e: e.path)

    out: list[str] = []
    for err in errors:
        path = "/".join(str(p) for p in err.absolute_path) or "<root>"
        out.append(f"{path}: {err.message}")
    return out
